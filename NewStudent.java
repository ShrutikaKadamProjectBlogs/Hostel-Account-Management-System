    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        String number=jTextField1.getText();
        String activate;
        String roomStatus="Not Booked";
   
        if(jCheckBox1.isSelected())
        {
            activate="Yes";
        }else{
            activate="No";}
        try{
            
            Connection con=DatabaseConnection .getCon();
            PreparedStatement ps = con.prepareStatement("insert into room values(?,?,?)");
            ps.setString(1, number);
             ps.setString(2,activate);
              ps.setString(3,roomStatus);
            ps.executeUpdate();
            
            JOptionPane.showMessageDialog(null,"sucessfully save");
            tabledetails();
            clear();
        }
        catch(Exception e){
             JOptionPane.showMessageDialog(null,e);
    }//GEN-LAST:event_jButton4ActionPerformed
    }
