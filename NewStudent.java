    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        // TODO add your handling code here:
        String number=jTextField2.getText();
        int i=0;
        try{
            Connection Con=DatabaseConnection.getCon();
            Statement st=Con.createStatement();
            ResultSet rs=st.executeQuery("select *from room where number='"+number+"'");
            while(rs.next())
            {
               i=1;
               if(rs.getString(3).equals("Booked"))
               {
                   JOptionPane.showMessageDialog(null,"Room number doesnot exist");
               clear();}
               else
                       jTextField2.setEditable(false);
                       jTextField2.setForeground(Color.RED);
                       jTextField2.setBackground(Color.PINK);
                
               
            
               
               if(rs.getString(2).equals("Yes"))
               
                 jCheckBox2.setSelected(true);
               
               else
                   jCheckBox2.setSelected(true);
            
            }
               
               if(i==0)
               {
               JOptionPane.showMessageDialog(null,"Room number doesnot exist");
               clear();
               
               }
              
            
            
        }catch(Exception e){
        
        JOptionPane.showMessageDialog(null, "Room number does not exist.");
        }
    }//GEN-LAST:event_jButton7ActionPerformed

